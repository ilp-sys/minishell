#include "../include/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	ret;
	size_t	i;

	ret = 0;
	i = 0;
	while (src[ret])
		ret++;
	if (size == 0)
		return (ret);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (ret);
}
